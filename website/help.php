<a id="about"></a><div class="rozdzial">About GeoKrety</div>

<p>GeoKrety (gr. <i>geo</i> = ‘earth’, krety <i>pol.</i> = ‘<a href="https://en.wikipedia.org/wiki/Mole_%28animal%29">moles</a>’) is a service similar to TravelBug(TM) or GeoLutins, aiming at tracking objects you leave in geocache containers. People move these registered objects (called GeoKrety) from cache to cache and register each move with this service. The trip of each GeoKret is then presented on a map.</p>

<p><img src="https://cdn.geokrety.org/images/help/kret-foto.jpg" alt="foto kreta" width="300" height="225" /><br /><a href="https://en.wikipedia.org/wiki/Mole_%28animal%29">A mole</a></p>

<p>How to pronounce geokrety? ;)<br /><embed type="application/x-shockwave-flash" src="https://www.google.com/reader/ui/3523697345-audio-player.swf" quality="best" flashvars="audioUrl=https://cdn.geokrety.org/sounds/geokrety.mp3" width="300" height="27"></embed></p>

<p>GeoKrets are not a trade item, you do not have to put something in the cache to take a GK, and you should not take something from a cache in trade for a GK. If you should take something from a cache, you should make a trade, and you can place the GK.</p>

<p>The service is absolutely free (contrary to TravelBug) and easy to use. A GeoKret can be anything, but in most cases it is a small puppet.</p>

<p>Please see (and edit, if necesary) the article about geokrety <a href="https://en.wikipedia.org/wiki/Geokrety">at Wikipedia</a>.</p>

