<p>Thanks to <a href="https://www2.jpl.nasa.gov/srtm/">SRTM</a> and <a href="https://edc.usgs.gov/products/elevation/gtopo30/gtopo30.html">GTOPO30</a> elevation data and lat/lon to country code conversion (<a href="http://www.geonames.org/">geonames</a>), each trip point has assigned an altitude and country code (a flag). According to that data, the altitude profile is drawn. Above data and plots are updated every 10-15 minutes.</p>


<a id="qr"></a><div class="rozdzial">QR Code</div>
<p>For easy logging dropping or grabbing during your trip, I\'ve added the possibility of attaching QR code on the GK label. QR Code is a kind of bar code, which can be "scanned" by your mobile phone, which will redirect you to the proper page at geokrety.org; you will not have to enter the tracking code, everything will be encrypted in the QR. <a href="https://en.wikipedia.org/wiki/QR_Code ">Read more about QR Code</a>.</p>

