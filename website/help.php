<a id="GKandCode"></a><div class="rozdzial">Reference Number and Tracking Code</div>
<ul>
<li><strong>Reference Number</strong> e.g. GK003F. It is a <u>public</u> number that may be used to refer a GeoKret. It consists of \'GK\' prefix and 4 digits in hexadecimal system. You can use it to search a specific GeoKret or to link to this GeoKret. It is visible to everyone.</li>

<li><strong>Tracking Code</strong> eg XF3ACS. It is a <u>secret</u> identification code which is required to post logs such as "Grab", "Drop" and others for a GeoKret. Only the owner and geocachers who found the GeoKret should know this code. It is not recommended to post it anywhere. Of course this code must be written on GeoKret\'s tag to allow finders to log it.</li>
</ul>



